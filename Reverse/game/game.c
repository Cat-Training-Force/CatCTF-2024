int check(const char *buf, const uint8_t *hints, int is_row)
{
    int pos = 0;
    for (int i = 0; i < 29; i++)
    {
        int cnt = 0;
        for (int j = 0; j < 29; j++)
        {
            if (buf[is_row ? i * 29 + j : i + j * 29] != '0')
                cnt++;
            else if (cnt && cnt != hints[pos++])
                return 1;
            else
                cnt = 0;
        }
        if ((cnt && cnt != hints[pos++]) || hints[pos++])
            return 1;
    }
    return 0;
}

